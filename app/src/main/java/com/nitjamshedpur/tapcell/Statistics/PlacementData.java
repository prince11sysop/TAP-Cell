package com.nitjamshedpur.tapcell.Statistics;

public class PlacementData {

    private int cse;
    private int ece;
    private int me;
    private  int eee;
    private int ce;
    private int mme;
    private int pie;

    public PlacementData(){

    }

    public PlacementData(int cse, int ece, int me, int eee, int ce, int mme, int pie) {
        this.cse = cse;
        this.ece = ece;
        this.me = me;
        this.eee = eee;
        this.ce = ce;
        this.mme = mme;
        this.pie = pie;
    }

    public int getCse() {
        return cse;
    }

    public void setCse(int cse) {
        this.cse = cse;
    }

    public int getEce() {
        return ece;
    }

    public void setEce(int ece) {
        this.ece = ece;
    }

    public int getMe() {
        return me;
    }

    public void setMe(int me) {
        this.me = me;
    }

    public int getEee() {
        return eee;
    }

    public void setEee(int eee) {
        this.eee = eee;
    }

    public int getCe() {
        return ce;
    }

    public void setCe(int ce) {
        this.ce = ce;
    }

    public int getMme() {
        return mme;
    }

    public void setMme(int mme) {
        this.mme = mme;
    }

    public int getPie() {
        return pie;
    }

    public void setPie(int pie) {
        this.pie = pie;
    }
}
